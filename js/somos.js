/* Inicio animaciones Jquery */

$("document").ready(function(){
    $(".parrafo__mision").hide(400);


    $(".titulo__mision").click(function(){
        $(".parrafo__mision").show(400);
        
    }); 


    $("document").ready(function(){
        $(".parrafo__vision").hide(400);
        
    });
        
    $(".titulo__vision").click(function(){
        $(".parrafo__vision").show(400);
        
    });


    $(".parrafo__vision").click(function(){
        $(".parrafo__vision").hide(400);
        
    });

    $(".parrafo__mision").click(function(){
        $(".parrafo__mision").hide(400);
        
    });
     
    
});


/* Final animaciones Jquery */